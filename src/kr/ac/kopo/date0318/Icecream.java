package kr.ac.kopo.date0318;

class Icecream {

	private String name; // 아이스크림명
	private int price; // 아이스크림가격

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}
}
